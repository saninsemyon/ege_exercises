for n in range(1, 100):
    s = bin(n)[2:]  # перевод в двоичную систему
    s = str(s)
    #print("n: ", n, " -> ", s[-1])
    if s[-1] == '0':
        s = '1' + s + '0'
    else:
        s = '11' + s + '11'
    r = int(s, 2)  # перевод в десятичную систему
    print("r: ", r)
    if r > 52:
        print(n)
        break


